"""        BBQ PLATTER SYSTEM (Function + If-Else)
        This function takes number of pieces and returns
        the platter size based on quantity rules.

"""

def bbq(pieces):
    if pieces >= 10:
        return "Full BBQ Platter 🔥"
    elif pieces >= 5:
        return "Half BBQ Platter"
    else:
        return "Small BBQ Plate!"

"""### Testing

"""

