"""        Q2. CUSTOMER SERVING SYSTEM (Loop)
        We loop through 5 customers, take their food order as input,call the menu() function, and print the result.



"""

print("       CUSTOMER SERVING SYSTEM")

for i in range(1, 6):                          # Loop from customer 1 to 5
    item = input(f"Customer {i} — Enter food item: ")
    result = menu(item)
    print(f"Customer {i} ordered {item.lower()}, {result}")

print()

"""
        KARHAI SPICE SYSTEM (If-Else Function)
        This function takes a spice level and returns the
        type of karhai based on the spice level.
"""

def karhai(spice_level):
    spice_level = spice_level.lower()

    if spice_level == "high":
        return "Very Spicy Karhai 🌶️🔥"
    elif spice_level == "medium":
        return "Normal Karhai 🍳"
    elif spice_level == "low":
        return "Mild Karhai"
    else:
        return "Invalid input!"

