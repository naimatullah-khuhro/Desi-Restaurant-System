print()

"""      CHAI SYSTEM (Function + If-Else)
      This function takes a sugar value (0, 1, or 2+)
      and returns the type of chai accordingly.

"""

def chai(sugar):
    if sugar == 0:
        return "No Sugar Chai ☕"
    elif sugar == 1:
        return "Normal Chai ☕"
    elif sugar >= 2:
        return "Sweet Chai 🍬☕"
    else:
        return "Invalid sugar level"

"""### Testing

"""

print("       CHAI SYSTEM TEST")


print(f"Sugar 0 → {chai(0)}")
print(f"Sugar 1 → {chai(1)}")
print(f"Sugar 2 → {chai(2)}")
print(f"Sugar 4 → {chai(4)}")

print()

"""      FINAL TASK — DESI RESTAURANT MANAGEMENT SYSTEM
      This is the complete combined system that:
      - Loops through 5 customers
      - Takes their food order
      - Calls the correct function based on order
      - Prints a neat, formatted output for each customer

"""

print("   🍽️  DESI RESTAURANT MANAGEMENT SYSTEM  🍽️")

for i in range(1, 6):                          # Loop for 5 customers
    print(f"\n👤 Customer {i}")
    print("-" * 40)

    # Step 1: Take the food item order
    item = input(f"  Enter food item (karhai/biryani/bbq/chai): ").lower()

    # Step 2: Call the menu() function for this item
    menu_result = menu(item)
    print(f"  Menu Status: {menu_result}")

    # Step 3: Based on the item, call extra functions

    if item == "karhai":
        # If item is karhai , ask for spice level
        spice = input("  Enter spice level (high/medium/low): ")
        karhai_result = karhai(spice)
        print(f"  Karhai Type: {karhai_result}")

    elif item == "bbq":
        # If item is bbq , ask for number of pieces
        pieces = int(input("  Enter number of BBQ pieces: "))
        bbq_result = bbq(pieces)
        print(f"  BBQ Platter: {bbq_result}")

    elif item == "chai":
        # If item is chai → ask for sugar level
        sugar = int(input("  Enter sugar level (0 / 1 / 2+): "))
        chai_result = chai(sugar)
        print(f"  Chai Type: {chai_result}")

    # biryani has no extra step — menu() result is enough

    print("-" * 40)

print()

print("Thank you! All customers have been served.")